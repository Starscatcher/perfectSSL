#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*bla;
	size_t	i;

	bla = (char*)b;
	i = 0;
	while (len > 0)
	{
		bla[i] = c;
		i++;
		len--;
	}
	return (b);
}
